class MyCalc:

    def add(self,x,y):
        """This function adds two numbers"""
        return x+y
    
    def substract(self,x,y):
        """This function substracts two numbers"""
        return x - y
    
    def multiply(self,x,y):
        """This functions multiply two numbers"""
        return x * y
    
    def divide(self,x,y):
        """This functions divide two numbers"""
        return x / y
    